const Test = require('./test')

// ----------------------------------------------
const fun = arr => {
    return arr.map(list =>
        list.reduce((a, b) => a > b ? a : b));
}

// ----------------------------------------------

const testCases = [
    { inp: [[13, 27, 18, 26], [4, 5, 1, 3], [32, 35, 37, 39], [1000, 1001, 857, 1]], res: [27, 5, 39, 1001] },
    { inp: [[4, 9, 1, 3], [13, 35, 18, 26], [32, 35, 97, 39], [1000000, 1001, 857, 1]], res: [9, 35, 97, 1000000] },
    { inp: [[17, 23, 25, 12], [25, 7, 34, 48], [4, -10, 18, 21], [-72, -3, -17, -10]], res: [25, 48, 21, -3] },
]

// run test
Test.assertArrAll(testCases, fun);